package src.basics;

public class Chardatatype {
    public static void main(String[] args){
        char cha = 'a';
        System.out.print("Character is: " + cha);

        // Typecasting
        int x = cha; // Implicit typecasting from char to int
        System.out.println("\nASCII value of character '" + cha + "' is: " + x);


    }
}
