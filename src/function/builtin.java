package src.function;

public class builtin {
    public static void main(String[] args){
        // math.wale
        System.out.println(Math.sqrt(16));
        System.out.println(Math.cbrt(16));
        System.out.println(Math.abs(-5));  // absolute value
        System.out.println(Math.max(5, 10));
        System.out.println(Math.floor(163.8987)); // greatest integer function
        System.out.println(Math.ceil(163.8987));  // smallest integer function
        System.out.println(Math.round(163.8987)); // round function
        System.out.println(Math.pow(2, 3)); // power function
        System.out.println(Math.random()); // random function

    }
    
}
